package com.tydic.traffic.constant;

/**
 * 存放照片地址的相关常量
 *
 * @author zhangjj
 * @create 2017-08-22 15:08
 **/
public class Photo {

    private String partyCulture;

    private String partyFee;

    private String news;

    private String talkNotes;

    private String partyModel;

    public String getPartyCulture() {
        return partyCulture;
    }

    public void setPartyCulture(String partyCulture) {
        this.partyCulture = partyCulture;
    }

    public String getPartyFee() {
        return partyFee;
    }

    public void setPartyFee(String partyFee) {
        this.partyFee = partyFee;
    }

    public String getNews() {
        return news;
    }

    public void setNews(String news) {
        this.news = news;
    }

    public String getTalkNotes() {
        return talkNotes;
    }

    public void setTalkNotes(String talkNotes) {
        this.talkNotes = talkNotes;
    }

    public String getPartyModel() {
        return partyModel;
    }

    public void setPartyModel(String partyModel) {
        this.partyModel = partyModel;
    }
}
